#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == (char )c)
			return (&str[i]);
		i++;
	}
	if ((char )c == '\0' && str[i] == '\0' )
		return (&str[i]);
	return (NULL);
}
